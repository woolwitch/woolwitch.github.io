export interface Order extends BaseOrder {
  status: 'pending' | 'paid' | 'shipped' | 'delivered' | 'cancelled';
  payment_method: 'card' | 'paypal';
}

// Order with items for display purposes
export interface OrderWithItems extends Order {
  order_items: OrderItem[];
  payments?: Payment[];
}

// Order item with product information
export type OrderItem = Database['woolwitch']['Tables']['order_items']['Row'];

// Base payment type from database
export type BasePayment = Database['woolwitch']['Tables']['payments']['Row'];

// Payment record type
export interface Payment extends BasePayment {
  status: 'pending' | 'completed' | 'failed' | 'refunded';
  payment_method: 'card' | 'paypal';
  currency: string;
}

// PayPal payment details structure
export interface PayPalDetails {
  paypal_order_id?: string;
  payer_id?: string;
  payer_email?: string;
  transaction_id?: string;
  capture_id?: string;
  gross_amount?: number;
  fee_amount?: number;
  net_amount?: number;
}

// Stripe payment details structure
export interface StripeDetails {
  payment_intent_id: string;
  payment_method_id?: string;
  last_four?: string;
  card_brand?: string;
  // Note: client_secret is intentionally excluded - should not be stored long-term
}

// Cart item interface (for order creation)
export interface CartItem {
  product: Product;
  quantity: number;
}

// Order creation data interface
export interface CreateOrderData {
  email: string;
  fullName: string;
  address: OrderAddress;
  cartItems: CartItem[];
  paymentMethod: 'card' | 'paypal';
  paymentId?: string; // For PayPal orders or Stripe payment intent ID
  paypalDetails?: PayPalDetails;
  stripeDetails?: StripeDetails;
}

// Order summary for display
export interface OrderSummary {
  subtotal: number;
  deliveryTotal: number;
  total: number;
  itemCount: number;
}

// Product type export
export type Product = Database['woolwitch']['Tables']['products']['Row'];

